export const getTranslation = (lang, key) => {
  const keys = key.split('.');
  let value = translations[lang] || translations.en;
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  return value || translations.en?.[keys[0]]?.[keys[1]] || key;
};

export const t = (key, lang = 'en') => getTranslation(lang, key);

